function code(lang: string) {
  const value = lang.toLowerCase().replace("_", "-")
  if (value === "zht" || value.startsWith("zh-tw") || value.startsWith("zh-hant")) return "zht"
  if (value === "br" || value.startsWith("pt-br")) return "br"
  const [base] = value.split("-")
  return base || "en"
}

export function text(lang: string, key: Key, vars?: Record<string, string>) {
  const raw = dict[code(lang)]?.[key] ?? en[key]
  if (!vars) return raw
  return raw.replace(/\{(\w+)\}/g, (match, name: string) => vars[name] ?? match)
}
